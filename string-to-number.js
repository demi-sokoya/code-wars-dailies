// We need a function that can transform a string into a number. What ways of achieving this do you know?
/**
 * #Pseudo-code
 * ------------
 *
 * FUNCTION stringToNumber(str)
 *      RETURN CONVERT str TO number
 * END FUNCTION
 *
 */

function stringToNumber(str) {
  return parseInt(str);
}

console.log(stringToNumber("1234"));
